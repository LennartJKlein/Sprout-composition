					<main class="article-content">
						<p>Jongeren van de generatie die zijn geboren na 1995-2000 noemen we tegenwoordig de generatie Y. In het Engels spreken we dat uit als Generation why. Met name dat “why” zegt meer dan je op het eerste gezicht zou denken. Daarover straks meer.</p>
						<p>Onlangs was er op tv een leuk programma waarin de overheid onze jongeren wilde informeren over veranderingen in onze maatschappij, en waarin ze jongeren “belangrijke” adviezen wilde geven over hoe je daar mee om kunt gaan. Geweldig dat de overheid dat doet. Vast prima dat de jeugd hierbij geholpen moet worden. Maar zou iemand zich hebben afgevraagd hoeveel personen uit de doelgroep hiermee zijn bereikt?</p>

						<blockquote id="quote-ruud-stelder">De concurrentie is er keihard, waardoor veel retailers weer stenen winkels beginnen.</blockquote>

						<p>Ook retailers die een aantrekkelijk aanbod hebben voor de jeugd doen nog steeds veel aan radio-, tv- en dagbladreclame om hun onweerstaanbare aanbod onder de aandacht te brengen van Gereration Y. Uit onderzoek blijkt echter dat je je communicatie budget letterlijk net zo goed in de sloot kunt gooien want deze generatie is langs deze kanalen vrijwel onbereikbaar!</p>
						<p>Bij een onlangs gehouden onderzoek onder 400 jongeren van deze generatie kwamen de volgende wellicht schokkende feiten naar boven;</p>
						<ol>
							<li>Op de vraag wie er de afgelopen maand had gekeken naar publieke tv-zenders reageerde slechts 4% positief.</li>
							<li>Op de vraag wie de afgelopen maand had geluisterd naar een publieke radiozender reageerde niemand positief!</li>
							<li>Slechts 7% had de afgelopen maand een papieren krant gelezen!</li>
							<li>Niemand had het lokale huis aan huisblad gelezen in de afgelopen maand!</li>
							<li>Minder dan 10% wil überhaupt iets weten van aanbiedingen. Als ze iets willen kopen gaan ze zoeken op Google naar de beste deal of de laagste prijs!</li>
							<li>Meer dan de helft laat zich informeren door filmpjes op youtube.</li>
							<li>Meer dan 80% kijkt alleen naar content op het moment dat het hun uitkomt via een ander medium dan een tv-toestel. Het meest kijken ze via internet naar content on demand via een pc. Laptop of tablet.</li>
							<li>Ad blokkers en het skippen van advertenties is bijna standaard.</li>
						</ol>
						<p>Hieruit blijkt dat deze generatie vrijwel onbereikbaar is geworden voor de boodschappen die overheden en bedrijven de wereld in willen sturen.</p>
						<h2 id="leuk-nuttig-authentiek">Leuk, nuttig en authentiek</h2>
						<p>En toch kun je wel degelijk nog contact krijgen met deze doelgroep. Maar dan moet je het wel anders aanpakken. Deze jongeren vinden het erg belangrijk dat je boodschap leuk, nuttig en bovenal authentiek is. "Waarom" is hierbij de kern. De belangrijkste vragen die ze zichzelf stellen: Waarom zou ik ernaar willen kijken? Waarom moet ik er enthousiast voor worden? Waarom is dit voor mij de moeite waard? Dus die Engelse uitspraak “why” doet er meer toe dan je zou denken!</p>
						<p>Leuke filmpjes met een boodschap die subtiel het bericht over brengen en die worden aangeboden via Youtube, Instagram, Netflix, en virals via email trekken nog wel degelijk de aandacht. Als het bericht maar cool is, dan praten ze er onderling volop over en gaan ze het ook onderling delen.</p>
						<p>Als je contact wilt maken en houden met deze doelgroep zul je dus heel veel moeten testen en het zeker totaal anders moeten aanpakken dan in het verleden. En dat terwijl deze doelgroep wel degelijk behoorlijk veel koopkracht heeft. Wil jij jouw marketingbudget dus niet rechtstreeks het riool in pompen en is generatie Y jouw doelgroep, dan zul je dus fors met je tijd mee moeten gaan! </p>
