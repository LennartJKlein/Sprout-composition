						<p>Veel winkeliers denken dat klanten niet meer naar hun winkel komen omdat webshops goedkoper zijn. Vreemd genoeg bleek dit uit onderzoek maar zelden het geval.</p>
						<p>Klanten zoeken niet perse de laagste prijs, maar ze willen wél de beste deal! Daarbij speelt vooral de totale koopbeleving een belangrijke rol.</p>
						<p>Voor dit onderzoek werden 2000 consumenten ondervraagd via een enquête, en werden 20 consumenten ondervraagd in een consumenten-panel. Hieronder lees je de 7 belangrijkste uitkomsten:</p>
						<h2>1. Welkom voelen</h2>
						<p>Consumenten willen zich welkom voelen in een winkel. Dat heeft te maken met sfeer, verlichting, uitstraling van de winkel, maar bovenal met hoe medewerkers ze verwelkomen in de winkel. Hoe voelt de begroeting door het personeel. Komt die begroeting oprecht gemeend over of is het een plichtmatige begroeting zonder gevoel en oprechte interesse. Gebrek aan de juiste begroeting bepaalt voor een groot deel of de klant je de omzet gunt!</p>
						
						<h2>2. Meer dan geld</h2>
						<p>Zie je de klant als mens of als een portemonnee waar je omzet uit moet trekken? Klanten willen voelen dat je ze echt wilt helpen en niet het idee krijgen dat je alleen uit bent op hun geld. Een veel gehoorde opmerking was dat consumenten het gevoel hebben dat één seconde na het afrekenen de aandacht al niet meer bij de klant is. Dat is vaak reden om niet meer of minder bij die winkel te komen.</p>
						<h2>3. Vakkennis</h2>
						<p>Als ze in de winkel geen kennis hebben van wat ze verkopen kun je beter naar een webshop gaan. Daar staat alle productinfo overzichtelijk op een rijtje en krijg ik zelfs alternatieve keuzes aangeboden. Krijgt een klant echter te maken met een verkoper met vakkennis en inlevingsvermogen in de echte behoefte van een klant, dan vinden klanten dat advies van echte toegevoegde waarde en zijn dan meestal graag bereid om meer te betalen.</p>
						<h2>4. Toegevoegde waarde</h2>
						<p>Als je in een winkel jouw toegevoegde waarde niet tastbaar of merkbaar kunt maken, waarom zouden klanten daar dan voor willen betalen? Een veel gehoorde klacht in het onderzoek is dat winkeliers te weinig moeite doen om een stapje extra te doen voor een klant. Bij minimale service hoort ook echt de minimale prijs, anders zoekt de klant elders wel de beste deal!</p>
						<h2>5. Cadeautje</h2>
						<p>Een belangrijke reden voor de klant om bij een winkel te kopen in plaats van bij een webshop is dat het gaat om een cadeau. Daarbij noemen ze twee redenen. De eerste is dat ruilen in een winkel gemakkelijker is dan bij een webshop. De ontvanger kan dan met minder moeite even zijn cadeau ruilen. Bovendien pakken webshops vrijwel nooit een cadeau leuk in. Als een winkel dat wel doet is dit een belangrijke reden om te kiezen voor die winkel. Zelfs als de klant eventueel iets extra moet betalen voor een luxe cadeauverpakking.</p>
						<h2>6. Kletsend personeel</h2>
						<p>Klanten haten personeel dat onderling praat tijdens het helpen van een klant! Erger nog vinden ze het als personeel “druk is op de telefoon” terwijl ze achter de toonbank of in de winkel staan. Voor veel klanten is dat één van de belangrijkste redenen om die winkel voortaan niet meer te bezoeken! Ook ergeren klanten zich enorm aan personeel dat niet achter de kassa of toonbank weg te slaan is terwijl de klant even advies vraagt of iets niet kan vinden.</p>
						<p>Meer dan de helft van de klanten gaf aan in dergelijke gevallen van de koop af te zien! Voor bijna dertig procent was dat ook reden om voortaan een dergelijke winkel minder te bezoeken! </p>
						<h2>7. Geen aftersales</h2>
						<p>Gebrek aan aftersales aandacht is voor veel klanten reden om een winkel de rug toe te keren. Ruil beleid en garantieafwikkeling zijn grote bronnen van ergernis. Online mag je alles terugsturen, maar ruilen in een winkel is vaak een drama, zeker als je iets wilt terugbrengen dat niet naar behoren werkt. Vaak maken winkels een defect het probleem van de klant. Dat moet u rechtstreeks met de fabrikant regelen horen klanten te vaak. De nummer één reden om nooit meer terug te komen!</p>
