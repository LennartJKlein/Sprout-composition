						<p>Een retaildiscussie bij maanlicht, dat is waar het deze maandagavond grofweg op neerkomt. Sprout mag aanschuiven voor een diner in het Amsterdamse Pulitzer Hotel, waar de Canadese Lightspeed-ceo Dax Dasilva en Neder-topman Ruud Stelder met Nederlandse ondernemers de stand van zaken binnen de retail bespreken. Lightspeed levert software voor ondernemers, en wil wel eens weten hoe zijn Nederlandse afnemers het doen.</p>
						<h2 id="winner-takes">The winner takes it all</h2>
						<p>Organiseer je een zakendiner, dan mag het je dinerpartners aan niets ontbreken. De Canadesen lijken dat te hebben begrepen. Het is een avond met servet op de broek, drie gangen, moeilijke ijsdesserts en zwijgzame obers die continu witte wijn bijschenken. We spotten één verdwaalde getatoeëerde arm, maar voor de rest vooral nette bloesjes en jasjes. Chique, zoals je het bij een Quote-feestje zou verwachten.</p>
						<p>Maar gelijk hebben ze, want vanavond gaat het dan ook over niets minder dan geldzaken. Want hoe verdien je tegenwoordig nog wat in de moeilijke sector van de retail? Stenen winkels vallen bij bosjes om, en online moet je eruit schieten om voldoende omzet te behalen. The winner takes all, en het idee van een winnaar is dat er doorgaans maar een van is. Deze avond staat in het teken van een discussie tussen retailondernemers die er daadwerkelijk in slagen het hoofd boven water te houden. Toch knap. Hoe doen ze dit?</p>
						<h2 id="millennials">Millennials</h2>
						<p>Aan tafel zitten opvallend veel millennials; Boris Blijham en Danny ten Nijenhuis van de Amsterdamse naar eigen zeggen “weinig hippe” telefoon-reperatieservice ThePhoneLab, e-commercemanager Dax Stokman van kledingmerk <a href="#">ETQ</a>, Randy Hoogeweegen van de Amsterdamse kledingwinkel <a href="#">ABOUT. lifestyle for men</a>, Mark Bos van motorpakkenwinkel <a href="#">Motorkledingcenter</a> en social influencer <a href="#">Sabine de Witte</a>, die – zoals dat gaat – voor de helft van de tijd aanwezig is, en voor de andere helft haar Instagram bijwerkt. Het leven van een influencer gaat niet over rozen. Bob Zwanikken van apparatuurwinkel Wifimedia is - zoals hij zelf al direct opmerkt - de enige ‘oudere’ retailer. Maar goed, hij werkt samen met Radio 538. Dat kan de rest dan weer niet zeggen.</p>
						<p>Met zoveel jonge retailers is het dan ook niet verwonderlijk dat het gesprek al snel over de onlinewereld gaat. Ooit, lang geleden, had je als retailer weinig problemen met klagende klanten. Immers, de ontevredenen zouden hun beklag misschien aan een beperkt aantal vrienden doen, maar ze hadden verder geen podium. In de internetwereld is dit anders. Iedereen realiseert: een vernietigende tweet of online-beoordeling op een platform kan funest zijn voor je omzet.</p>
